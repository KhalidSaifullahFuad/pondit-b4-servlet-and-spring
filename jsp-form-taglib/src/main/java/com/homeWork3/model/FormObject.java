package com.homeWork3.model;

public class FormObject {
    private String textField;
    private String date;
    private String ddSelectedOption;

    private String email;


    public String getTextField() {
        return textField;
    }

    public void setTextField(String textField) {
        this.textField = textField;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDdSelectedOption() {
        return ddSelectedOption;
    }

    public void setDdSelectedOption(String ddSelectedOption) {
        this.ddSelectedOption = ddSelectedOption;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }


}
